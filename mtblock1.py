class LRB(nn.Module):
    def __init__(self, in_channels, out_channels):
        super().__init__()

        self.in_layers = nn.Sequential(
            nn.GroupNorm(32, in_channels),
            nn.SiLU(),
            nn.Conv2d(in_channels, out_channels, kernel_size=5, padding=2),
        )

        self.out_layers = nn.Sequential(
            nn.GroupNorm(32, out_channels),
            nn.SiLU(),
            nn.Conv2d(out_channels, out_channels, kernel_size=3, padding=5, dilation=5),
        )

        if out_channels == in_channels:
            self.skip = nn.Identity()
        else:
            self.skip = nn.Conv2d(in_channels, out_channels, kernel_size=1)

    def forward(self, x):
        h = self.in_layers(x)
        h = self.out_layers(h)
        return h + self.skip(x)

class MTBlock(nn.Module):
    def __init__(self,in_channels, out_channels):
        super().__init__()
        self.srb = SRB(in_channels, out_channels)
        self.mrb = MRB(in_channels, out_channels)
        self.lrb = LRB(in_channels, out_channels)
        self.conv = BasicConv2d(in_channels, out_channels)

    def forward(self,x):
        x1 = self.srb(x)
        x2 = self.mrb(x)
        x3 = self.lrb(x)

        x = x1 + x2 + x3

        out1 = self.conv(x)
        return out1